const puppeteer = require("puppeteer-extra");

async function run() {
  const data = $json;

  // --- Get values ---
  const title = data.title || "Untitled";
  const teaser = data.teaser || "";

  // --- HTML Template ---
  const html = `
  <html>
  <head>
    <meta charset="utf-8">
    <style>
      body {
        font-family: Arial, sans-serif;
        padding: 60px 40px;
        color: #222;
        line-height: 1.6;
      }
      h1 {
        font-size: 32px;
        margin-bottom: 20px;
      }
      .teaser {
        font-size: 16px;
        color: #444;
        margin-top: 10px;
        white-space: pre-line; /* preserves line breaks */
      }
      .footer {
        position: fixed;
        bottom: 20px;
        left: 40px;
        right: 40px;
        font-size: 12px;
        color: #999;
        text-align: center;
      }
    </style>
  </head>
  <body>

    <h1>${title}</h1>

    <div class="teaser">
      ${teaser}
    </div>

    <div class="footer">
      Generated by n8n • ${new Date().toLocaleDateString()}
    </div>

  </body>
  </html>
  `;

  // --- Launch browser ---
  const browser = await puppeteer.launch({
    headless: true,
    args: ["--no-sandbox", "--disable-setuid-sandbox"],
  });

  const page = await browser.newPage();

  // --- Load content ---
  await page.setContent(html, { waitUntil: "networkidle0" });

  // --- Generate PDF ---
  const pdfBuffer = await page.pdf({
    format: "A4",
    printBackground: true,
    margin: {
      top: "25mm",
      bottom: "25mm",
      left: "20mm",
      right: "20mm",
    },
  });

  await browser.close();

  // --- Return binary ---
  return [
    {
      binary: {
        data: pdfBuffer.toString("base64"),
        mimeType: "application/pdf",
        fileName: `document_${Date.now()}.pdf`,
      },
    },
  ];
}

return run();
